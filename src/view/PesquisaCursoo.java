    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        CursoDAO dao = new CursoDAO();
        mostraBanco.clear();
        mostraBanco = dao.recuperaTodosCursos();

        DefaultTableModel model = (DefaultTableModel) tblPesquisa.getModel();

        for (Cursoo c : mostraBanco) {
            model.addRow(new Object[]{c.getNome(), c.getArea(), c.getCargaHoraria(), c.getValor(), c.getValorMensal(), c.getNumeroVagas()});
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        CursoDAO dao = new CursoDAO();
        mostraBanco = dao.pesquisaCursos(txtPesquisa.getText());
        DefaultTableModel model = (DefaultTableModel) tblPesquisa.getModel();
        model.setNumRows(0);
        for (Cursoo c : mostraBanco) {
            model.addRow(new Object[]{c.getNome(), c.getArea(), c.getCargaHoraria(), c.getValor(), c.getValorMensal(), c.getNumeroVagas()});
        }
    }//GEN-LAST:event_btnPesquisaActionPerformed


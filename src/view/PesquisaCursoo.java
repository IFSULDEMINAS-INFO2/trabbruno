    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:

        int linha = tblPesquisa.getSelectedRow();
        System.out.println(linha);
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um curso na tabela!!");

        } else {
           
            int id = mostraBanco.get(linha).getIdCurso();
            int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir o curso?");
            if (opcao == 0) {
                
                CursoDAO dao = new CursoDAO();
                boolean retorno = dao.excluirCurso(id);
               
                if (retorno) {
                    DefaultTableModel model = (DefaultTableModel) tblPesquisa.getModel();
                    model.removeRow(linha);
                    JOptionPane.showMessageDialog(this, "ExclusÃ£o com sucesso");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir");
                }
            }
        }

    }//GEN-LAST:event_btnExcluirMouseClicked


    private void btnGravarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarCursoActionPerformed
        Cursoo curso = new Cursoo();
        curso.setNome(txfNomeCurso.getText());
        curso.setCodigo(txfCodigoCurso.getText());
        curso.setValor(txfValorCurso.getText());
        curso.setCargaHoraria(txfCargaHorariaCurso.getText());
        curso.setNumeroVagas(txfNumerodeVagas.getText());
        curso.setValorMensal(txfValorMensal.getText());
        curso.setArea(txfArea.getText());
        
        
        
        CursoDAO dao = new CursoDAO();
        dao.gravarCurso(curso);         
    }//GEN-LAST:event_btnGravarCursoActionPerformed

